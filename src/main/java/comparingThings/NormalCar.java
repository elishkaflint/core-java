package comparingThings;

public class NormalCar {

    private String name;
    private int maxSpeed;

    public NormalCar(String name, int maxSpeed) {
        this.name = name;
        this.maxSpeed = maxSpeed;
    }

    public String getName() {
        return name;
    }

    public int getMaxSpeed() {
        return maxSpeed;
    }

}
